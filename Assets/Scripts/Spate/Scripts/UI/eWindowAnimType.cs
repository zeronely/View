using System;

public enum eWindowAnimType
{
    /// <summary>
    /// 无动画
    /// </summary>
    None,
    /// <summary>
    /// 渐变动画
    /// </summary>
    Alpha,
    /// <summary>
    /// 缩放动画
    /// </summary>
    Scale,
    /// <summary>
    /// 从上到下移动
    /// </summary>
    UpToDown,
    /// <summary>
    /// 从下到上移动
    /// </summary>
    DownToUp,
    /// <summary>
    /// 从左到右
    /// </summary>
    LiftToRight,
    /// <summary>
    /// 从右到左移动
    /// </summary>
    RightToLeft,
}

