 using System;
 
 
 /// <summary>
 /// 窗口动画类型
 /// </summary>
 public enum WindowMotion
 {
     /// <summary>
     /// 无动画
     /// </summary>
     None,
     /// <summary>
     /// 渐变动画
     /// </summary>
     Alpha,
     /// <summary>
     /// 缩放动画
     /// </summary>
     Scale,
     /// <summary>
     /// 偏移动画
     /// </summary>
     Offset
 }